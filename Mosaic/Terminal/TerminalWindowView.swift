    private var matchTotal: Int = 0
    private var matchIndex: Int = 0  // 1-based; 0 means no current match

    // MARK: - Notification rate limiter

    /// Rolling-window counter limiting OSC 777 notifications to ≤ 10 per minute.
    /// Prevents a PTY process from spamming the user's macOS notification centre.
    /// Uses monotonic systemUptime so an NTP step or manual clock change cannot
    /// reset the window and allow an immediate burst bypass.
    private var notificationCount = 0
    private var notificationWindowStart: Double = -.infinity

    /// Rolling-window counter limiting BEL to ≤ 10 per minute.
    /// Mirrors the OSC 777 cap so a PTY cannot bypass the limit by using raw BEL.
    private var bellCount = 0
    private var bellWindowStart: Double = -.infinity

    func showFindBar() {
        let bar = ensureFindBar()
        if bar.searchText.isEmpty,
           let selected = termView.getSelection(),
           !selected.isEmpty,
           !selected.contains("\n") {
            bar.searchText = selected
            recomputeMatchTotal(term: selected)
            if matchTotal > 0 {
                matchIndex = 1
                _ = termView.findNext(selected)
            }
        }
        bar.setMatchInfo(current: matchIndex, total: matchTotal)
        bar.isHidden = false
        bar.focus()
    }

    func hideFindBar() {
        findBar?.isHidden = true
        termView.clearSearch()
        matchTotal = 0
        matchIndex = 0
        window?.makeFirstResponder(termView)
    }

    func findNext() {
        guard let bar = findBar, !bar.searchText.isEmpty else {
            showFindBar()
            return
        }
        // Recompute on every action — scrollback grows under streaming output, and
        // our counter would otherwise drift away from SwiftTerm's internal cursor.
        recomputeMatchTotal(term: bar.searchText)
        guard matchTotal > 0 else {
            matchIndex = 0
            bar.setMatchInfo(current: 0, total: 0)
            return
        }
        matchIndex = matchIndex >= matchTotal ? 1 : matchIndex + 1
        _ = termView.findNext(bar.searchText)
        bar.setMatchInfo(current: matchIndex, total: matchTotal)
    }

    func findPrevious() {
        guard let bar = findBar, !bar.searchText.isEmpty else {
            showFindBar()
            return
        }
        recomputeMatchTotal(term: bar.searchText)
        guard matchTotal > 0 else {
            matchIndex = 0
            bar.setMatchInfo(current: 0, total: 0)
            return
        }
        matchIndex = matchIndex <= 1 ? matchTotal : matchIndex - 1
        _ = termView.findPrevious(bar.searchText)
        bar.setMatchInfo(current: matchIndex, total: matchTotal)
    }

    private func ensureFindBar() -> TerminalFindBar {
        if let bar = findBar { return bar }
        let bar = TerminalFindBar()
        bar.translatesAutoresizingMaskIntoConstraints = false
        bar.isHidden = true
        bar.onSearchChanged = { [weak self] term in
            guard let self else { return }
            if term.isEmpty {
                self.termView.clearSearch()
                self.matchTotal = 0
                self.matchIndex = 0
            } else {
                self.recomputeMatchTotal(term: term)
                if self.matchTotal > 0 {
                    self.matchIndex = 1
                    _ = self.termView.findNext(term)
                } else {
                    self.matchIndex = 0
                    self.termView.clearSearch()
                }
            }
            self.findBar?.setMatchInfo(current: self.matchIndex, total: self.matchTotal)
        }
        bar.onFindNext = { [weak self] in self?.findNext() }
        bar.onFindPrevious = { [weak self] in self?.findPrevious() }
        bar.onClose = { [weak self] in self?.hideFindBar() }
        addSubview(bar)
        NSLayoutConstraint.activate([
            bar.topAnchor.constraint(equalTo: topAnchor, constant: titleBarHeight + 4),
            bar.trailingAnchor.constraint(equalTo: trailingAnchor, constant: -6),
            bar.leadingAnchor.constraint(greaterThanOrEqualTo: leadingAnchor, constant: 6),
            bar.widthAnchor.constraint(lessThanOrEqualToConstant: 420)
        ])
        findBar = bar
        return bar
    }

    /// Count case-insensitive substring occurrences across the entire scrollback +
    /// visible buffer. SwiftTerm's full-buffer match count is internal, so we walk
    /// rows via the public getScrollInvariantLine and tally matches per line.
    private func recomputeMatchTotal(term: String) {
        guard !term.isEmpty, let terminal = termView.terminal else {
            matchTotal = 0
            return
        }
        // getScrollInvariantLine's valid row range is contiguous starting at
        // some non-negative `linesTop`. Find it with a doubling probe + binary
        // search, then walk forward until the function returns nil again.
        let firstRow: Int
        if terminal.getScrollInvariantLine(row: 0) != nil {
            firstRow = 0
        } else {
            var hi = 1
            while terminal.getScrollInvariantLine(row: hi) == nil {
                if hi > 1 << 24 { matchTotal = 0; return }
                hi <<= 1
            }
            var lo = hi >> 1
            while lo < hi {
                let mid = (lo + hi) >> 1
                if terminal.getScrollInvariantLine(row: mid) == nil {
                    lo = mid + 1
                } else {
                    hi = mid
                }
            }
            firstRow = lo
        }

        var count = 0
        var bytesScanned = 0
        var row = firstRow
        let lower = term.lowercased()
        let perRowCap = Self.maxScrollbackLineChars
        // Cap at 10 000 rows / 1 MB of text so a PTY that fills scrollback cannot
        // block the MainActor for hundreds of milliseconds per find-bar keystroke.
        let totalCap = 1 * 1024 * 1024
        while let line = terminal.getScrollInvariantLine(row: row) {
            var s = line.translateToString(
                trimRight: true,
                characterProvider: { let c = $0.getCharacter(); return c == "\0" ? " " : c }
            ).lowercased()
            if s.count > perRowCap { s = String(s.prefix(perRowCap)) }
            bytesScanned += s.utf8.count
            if !s.isEmpty {
                var idx = s.startIndex
                while let r = s.range(of: lower, range: idx..<s.endIndex) {
                    count += 1
                    idx = r.upperBound
                }
            }
            row += 1
            if row - firstRow > 10_000 || bytesScanned > totalCap { break }
        }
        matchTotal = count
    }

    /// Clear scrollback and screen (Cmd+K behaviour). Feeds ESC sequences directly
    /// to the terminal display — does not send anything to the PTY.
    func clearScrollback() {
        // ESC[H  = cursor home
        // ESC[2J = erase screen
        // ESC[3J = erase saved lines (scrollback)
        termView.feed(byteArray: [0x1B, 0x5B, 0x48,
                                  0x1B, 0x5B, 0x32, 0x4A,
                                  0x1B, 0x5B, 0x33, 0x4A])
    }

    private func startProcess() {
        let settings = TerminalSettings.shared
        applyTerminalSettings(settings)

        var env = ProcessInfo.processInfo.environment
        // Strip variables that commonly hold credentials from Mosaic's own
        // process environment (e.g. API keys injected by CI runners or
        // launch-agent wrappers) so they don't leak into spawned shells.
        // Standard shell variables (PATH, HOME, USER, SHELL, LANG, …) are
        // unaffected. Suffix matching is case-insensitive to catch mixed-case
        // naming conventions.
        // Suffix-based stripping (case-insensitive).
        // "_KEY"    catches OPENAI_KEY, ANTHROPIC_KEY, PRIVATE_KEY, etc.
        // "_AUTH"   catches GH_AUTH, GITHUB_AUTH, etc.
        // "_KEY_ID" catches AWS_ACCESS_KEY_ID, etc.
        let credentialSuffixes = [
            "_TOKEN", "_SECRET", "_API_KEY", "_PASSWORD", "_PASS", "_CREDENTIAL",
            "_KEY", "_AUTH", "_KEY_ID",
        ]
        // Exact-name matching for well-known credential vars whose names don't
        // follow a suffix pattern (e.g. DATABASE_URL, where "_URL" alone is too broad).
        let exactCredentialNames: Set<String> = [
            "DATABASE_URL",   // common 12-factor DB connection strings
        ]
        for key in env.keys {
            let upper = key.uppercased()
            if credentialSuffixes.contains(where: { upper.hasSuffix($0) })
                || exactCredentialNames.contains(upper) {
                env.removeValue(forKey: key)
            }
        }
        env["TERM"] = "xterm-256color"
        env["COLORTERM"] = "truecolor"

        var isDir: ObjCBool = false
        let cwdExists = FileManager.default.fileExists(atPath: currentCwd, isDirectory: &isDir) && isDir.boolValue
        let cwd = cwdExists ? currentCwd : FileManager.default.homeDirectoryForCurrentUser.path

        let execName = "-" + (shell as NSString).lastPathComponent
        let envList  = env.map { "\($0.key)=\($0.value)" }
        let shellPath = shell
        let scrollbackLines = settings.scrollbackLines
        let cursorStyle = settings.swiftTermCursorStyle

        // Cache window size before background dispatch — startProcess calls getWindowSize()
        // which reads self.frame; that must happen on the main thread.
        termView!.cachedWindowSize = termView!.getWindowSize()

        // Run forkpty on a background thread so the ~2 s atfork-handler cost
        // (libBacktraceRecording) doesn't block the main thread and cause a beachball.
        // SwiftTerm annotates startProcess as @MainActor but the underlying forkpty
        // call is thread-safe; the annotation is overly conservative.
        //
        // NOTE: Swift 6 emits "main actor-isolated method called in nonisolated
        // context" here. We treat this as the documented-acceptable exception in
        // CLAUDE.md (third-party API with an overly strict actor annotation we
        // can't change). `@preconcurrency import SwiftTerm` already downgrades
        // the error to a warning; further suppression would require unsafeBitCast
        // hacks that hurt readability more than the warning does.
        DispatchQueue.global(qos: .userInitiated).async { [termView] in
            termView!.startProcess(
                executable: shellPath,
                args: [],
                environment: envList,
                execName: execName,
                currentDirectory: cwd
            )
            DispatchQueue.main.async {
                // Clear the cached size now that the process is running.
                // sizeChanged() calls getWindowSize() to build the TIOCSWINSZ winsize;
                // from this point it must read the live frame, not the startup snapshot.
                termView!.cachedWindowSize = nil
                termView!.changeScrollback(scrollbackLines)
                termView!.getTerminal().setCursorStyle(cursorStyle)
            }
        }

        // Apply theme and optionally restore scrollback after shell init sequences
        // have run. The 2 s delay is a heuristic; if the shell takes longer to
        // initialize, restored scrollback may still interleave with startup output.
        // A more robust fix would hook OSC 133 A (prompt-start) to trigger
        // restoration, but that requires shells to emit OSC 133 support — not
        // universal enough to rely on as the sole mechanism.
        DispatchQueue.main.asyncAfter(deadline: .now() + 2.0) { [weak self] in
            guard let self else { return }
            self.applyTheme(self.theme)
            if let raw = self.restoredScrollback, !raw.isEmpty {
                self.restoredScrollback = nil
                // Cap restored bytes — workspace.json is untrusted on disk.
                let text = raw.count > Self.maxScrollbackTotalChars
                    ? String(raw.suffix(Self.maxScrollbackTotalChars))
                    : raw
                // Strip all ANSI/OSC escape sequences (incl. 8-bit C1) from the
                // persisted scrollback before re-feeding it. Without this, a tampered
                // workspace.json could inject OSC sequences (OSC 8 hyperlinks, OSC 1337
                // file transfers, title spoofing) into the running terminal.
                // NUL bytes (U+0000) arise from terminal cells never explicitly written
                // (cursor-positioned TUI layouts). Replace them with spaces.
                let safe = TerminalWindowView.stripEscapeSequences(text)
                    .replacingOccurrences(of: "\u{0000}", with: " ")
                // Dim color + italic, then show saved content, then reset.
                // Prepend ST (ESC \) to close any dangling OSC/DCS sequence that
                // stripEscapeSequences may have left at the truncation boundary —
                // without it a bare ESC ] at the end would absorb the SGR reset as
                // the OSC body, leaving the terminal permanently dim/italic.
                let dimmed = "\u{1B}[2;3m" + safe + "\u{1B}\\\u{1B}[0m\r\n"
                self.termView.feed(text: dimmed)
            }
        }
    }

    /// Remove ANSI CSI sequences (`ESC [ … m/A/B/…`), OSC sequences
    /// (`ESC ] … BEL/ST`), and bare `ESC X` two-character sequences from `text`.
    /// Also handles the 8-bit C1 single-byte equivalents (0x9B CSI, 0x9D OSC,
    /// 0x90 DCS, 0x9E PM, 0x9F APC, 0x98 SOS) which xterm-class parsers (including
    /// SwiftTerm) honour and which UTF-8-encode to U+0080–U+009F (two bytes
    /// 0xC2 0x9X). A tampered workspace.json scrollback containing raw 0x9D…BEL
    /// would otherwise inject an OSC sequence when re-fed at launch.
    static func stripEscapeSequences(_ text: String) -> String {
        var result = ""
        result.reserveCapacity(text.count)
        var iter = text.unicodeScalars.makeIterator()
        while let ch = iter.next() {
            // 7-bit ESC-introduced or 8-bit C1 single-byte introducer.
            if ch == "\u{1B}" {
                guard let next = iter.next() else { break }
                consumeEscapeBody(intro: next, iter: &iter)
            } else if (0x80...0x9F).contains(ch.value) {
                // Map C1 to its 7-bit equivalent introducer and consume.
                let mapped: Unicode.Scalar
                switch ch.value {
                case 0x9B: mapped = "["
                case 0x9D: mapped = "]"
                case 0x90: mapped = "P"
                case 0x9E: mapped = "^"
                case 0x9F: mapped = "_"
                case 0x98: mapped = "X"
                default:
                    // Other C1 controls — just drop the single byte.
                    continue
                }
                consumeEscapeBody(intro: mapped, iter: &iter)
            } else {
                result.unicodeScalars.append(ch)
            }
        }
        return result
    }

    /// Consume the body of an escape sequence given its post-introducer class byte.
    /// Mutating because the iterator is advanced.
    private static func consumeEscapeBody(intro: Unicode.Scalar,
                                          iter: inout String.UnicodeScalarView.Iterator) {
        switch intro {
        case "[":
            // CSI: consume until a final byte in 0x40–0x7E.
            while let c = iter.next() {
                if c.value >= 0x40 && c.value <= 0x7E { break }
            }
        case "]", "P", "X", "^", "_":
            // ST-terminated: consume until BEL, ESC \, or 8-bit ST (U+009C).
            var prev: Unicode.Scalar = "\u{00}"
            while let c = iter.next() {
                if c == "\u{07}" || c == "\u{9C}" { break }
                if prev == "\u{1B}" && c == "\\" { break }
                prev = c
            }
        default:
            break       // Two-character escape; introducer already consumed.
        }
    }

