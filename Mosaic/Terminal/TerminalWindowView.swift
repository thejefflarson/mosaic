/// Structured log for security-relevant events in the terminal layer.
/// Auditable via Console.app (subsystem filter: bundle ID, category: "security").
private let securityLog = Logger(
    subsystem: Bundle.main.bundleIdentifier ?? "com.mosaic.CanvasTerm",
    category: "security"
)

/// Thin subclass that intercepts outgoing key data for broadcast mode
/// while still calling super to let the PTY receive input normally.
final class InterceptingTerminalView: LocalProcessTerminalView {
    var onSendData: (@Sendable (ArraySlice<UInt8>) -> Void)?
    /// Set to true while programmatically sending broadcast data so we don't
    /// re-trigger onSendData and cause an infinite broadcast loop.
    var suppressBroadcast = false

    /// Cached before background dispatch — startProcess calls getWindowSize() which
    /// reads self.frame; NSView.frame must only be accessed on the main thread.
    /// OSAllocatedUnfairLock is Sendable and synchronises access between the main
    /// thread (write/clear) and the background startProcess thread (read via
    /// getWindowSize).
    private let _windowSizeBox = OSAllocatedUnfairLock<winsize?>(initialState: nil)

    nonisolated var cachedWindowSize: winsize? {
        get { _windowSizeBox.withLock { $0 } }
        set { _windowSizeBox.withLock { $0 = newValue } }
    }

    override func getWindowSize() -> winsize {
        cachedWindowSize ?? super.getWindowSize()
    }

    /// Intercept outgoing data for broadcast. `send(source:data:)` is called for both
    /// user keystrokes and SwiftTerm's internal terminal protocol responses (DA1, DA2,
    /// CPR, etc.). Filter out protocol responses by their DEC private prefix (ESC [ ?
    /// or ESC [ >) — these sequences are exclusively generated by the terminal emulator
    /// in response to application queries and are never produced by user keyboard input.
    override func send(source: TerminalView, data: ArraySlice<UInt8>) {
        super.send(source: source, data: data)   // PTY receives input as normal
        guard !suppressBroadcast, Self.shouldBroadcast(data) else { return }
        onSendData?(data)
    }

    /// Return `true` if `data` looks like a user keystroke worth broadcasting,
    /// `false` if it's a terminal protocol response that should stay local.
    ///
    /// Filtered prefixes (never produced by user keystrokes):
    /// - `ESC [ ?` / `ESC [ >` — DEC private mode prefixes (DA1, DA2, XTWINOPS, CPR).
    /// - `ESC P / ] / X / ^ / _` — DCS / OSC / SOS / PM / APC introducers.
    /// - 8-bit C1 (`0x90–0x9F`) — single-byte equivalents of the above.
    ///
    /// Empty slices are also rejected — SwiftTerm hands us zero-byte slices on
    /// some paste/insertText paths and subscripting an empty slice traps.
    static func shouldBroadcast(_ data: ArraySlice<UInt8>) -> Bool {
        guard !data.isEmpty else { return false }
        let b0 = data[data.startIndex]
        if b0 == 0x1B, data.count >= 2 {
            let b1 = data[data.startIndex + 1]
            switch b1 {
            case 0x50, 0x5D, 0x58, 0x5E, 0x5F:        // DCS, OSC, SOS, PM, APC
                return false
            case 0x5B where data.count >= 3:           // CSI — DEC private + protocol responses
                let b2 = data[data.startIndex + 2]
                if b2 == 0x3F || b2 == 0x3E { return false }
                // Block intermediate-byte sequences (e.g. DECSTR ESC[!p).
                if b2 >= 0x20 && b2 <= 0x2F { return false }
                // Block CPR (cursor position report ESC[row;colR) — terminal-generated.
                // Verify that last byte is 'R' AND all intermediate bytes are ASCII
                // digits or ';' to avoid suppressing unrelated CSI sequences that
                // happen to end in 0x52 (e.g. DECRQSS responses with arbitrary data).
                if data[data.index(before: data.endIndex)] == 0x52 {
                    let inner = data[(data.startIndex + 2) ..< data.index(before: data.endIndex)]
                    if inner.allSatisfy({ ($0 >= 0x30 && $0 <= 0x39) || $0 == 0x3B }) {
                        return false
                    }
                }
            default: break
            }
        } else if b0 >= 0x90 && b0 <= 0x9F {           // 8-bit C1 (DCS … APC)
            return false
        }
        return true
    }

    /// SwiftTerm's default linefeed clears selection on every output line, making it
    /// impossible to select text while the terminal is streaming output. Don't propagate —
    /// selection is cleared appropriately by keyDown and mouseDown instead.
    override func linefeed(source: Terminal) {}

    /// SwiftTerm's feedPrepare() clears selection when allowMouseReporting is true,
    /// which kills any active selection every time terminal data arrives. Temporarily
    /// disable allowMouseReporting around the feed so the selection persists — the
    /// same approach iTerm2 and Terminal.app use to keep selection alive during output.
    override func dataReceived(slice: ArraySlice<UInt8>) {
        let wasReporting = allowMouseReporting
        if wasReporting && selectionActive {
            allowMouseReporting = false
        }
        super.dataReceived(slice: slice)
        if wasReporting && !allowMouseReporting {
            allowMouseReporting = wasReporting
        }
    }

    var onBell: (() -> Void)?

    /// Rolling-window counter limiting OSC 8 link-open confirmation dialogs to
    /// ≤ 5 per minute. A hostile PTY can emit arbitrary OSC 8 sequences; without
    /// a cap each one forces a modal NSAlert on the main thread, enabling alert
    /// fatigue and a main-thread denial-of-service.
    private var linkOpenCount = 0
    private var linkOpenWindowStart: Double = -.infinity

    /// Trim trailing whitespace from each copied line. SwiftTerm includes empty
    /// cells (padded with spaces) past the last printed character on each row,
    /// which is almost never what the user wants on the clipboard.
    override func copy(_ sender: Any) {
        let raw = getSelection() ?? ""
        guard !raw.isEmpty else {
            super.copy(sender)
            return
        }
        let pb = NSPasteboard.general
        pb.clearContents()
        pb.setString(Self.sanitizeForClipboard(Self.trimTrailingSpacesPerLine(raw)),
                     forType: .string)
    }

    /// Strip Unicode characters that allow malicious PTY output to display one thing
    /// and paste another: bidi overrides (U+202A–U+202E, U+2066–U+2069), zero-width
    /// chars (U+200B–U+200D, U+2060, U+FEFF), and the line/paragraph separators
    /// (U+2028/U+2029) that some shells treat as newlines. Defends against the
    /// "innocuous-looking command pastes as `rm -rf …`" class of attack
    /// (cf. iTerm2 2017, CVE-2017-2671).
    ///
    /// NFKC normalisation is applied first to collapse full-width ASCII forms
    /// (ａ→a, ０→0, …) and other compatibility variants that could otherwise
    /// encode visually-identical-but-different shell commands (homoglyph attack).
    /// Note: cross-script confusables (e.g. Cyrillic а vs Latin a) are not
    /// eliminated by NFKC; they require an external confusable database.
    nonisolated static func sanitizeForClipboard(_ text: String) -> String {
        // NFKC: decomposes compatibility characters then recomposes canonically,
        // mapping full-width/half-width variants to standard ASCII equivalents.
        let normalized = (text as NSString).precomposedStringWithCompatibilityMapping
        var out = String.UnicodeScalarView()
        out.reserveCapacity(normalized.unicodeScalars.count)
        for s in normalized.unicodeScalars {
            switch s.value {
            case 0x00...0x08, 0x0B...0x0C, 0x0E...0x1F,      // C0 controls (keep \t \n \r)
                 0x7F,                                         // DEL
                 0x80...0x9F:                                  // 8-bit C1 (incl. ESC variants)
                continue
            case 0x202A...0x202E, 0x2066...0x2069,            // bidi overrides / isolates
                 0x200B...0x200D, 0x2060, 0xFEFF,             // zero-width
                 0xFE00...0xFE0F,                              // variation selectors
                 0xE0100...0xE01EF,                            // variation selectors supplement
                 0xE0000...0xE007F:                            // Unicode TAG block
                continue
            case 0x2028, 0x2029:                              // line/paragraph separator
                out.append("\n")
            default:
                out.append(s)
            }
        }
        return String(out)
    }

    nonisolated static func trimTrailingSpacesPerLine(_ text: String) -> String {
        // Operate on UnicodeScalars: in the Character view, "\r\n" is a single
        // grapheme cluster, so splitting on the Character "\n" misses CRLF lines.
        text.unicodeScalars
            .split(separator: "\n", omittingEmptySubsequences: false)
            .map { scalars -> String in
                var s = String(String.UnicodeScalarView(scalars))
                let hadCR = s.hasSuffix("\r")
                if hadCR { s.removeLast() }
                while s.last == " " { s.removeLast() }
                if hadCR { s.append("\r") }
                return s
            }
            .joined(separator: "\n")
    }

    /// Clamp and strip control bytes from a notification field before forwarding to
    /// UNUserNotificationCenter — the payload comes from terminal output.
    /// Also strips bidi override/isolate codepoints and zero-width characters to
    /// prevent spoofed notification text (e.g. RLO-based direction reversal).
    nonisolated static func sanitizeNotificationText(_ s: String, max: Int) -> String {
        var out = String.UnicodeScalarView()
        out.reserveCapacity(min(s.unicodeScalars.count, max))
        var count = 0
        for scalar in s.unicodeScalars where count < max {
            let v = scalar.value
            if v < 0x20 && v != 0x0A { continue }              // strip C0 except newline
            if v >= 0x80 && v <= 0x9F { continue }             // strip 8-bit C1
            // Strip bidi overrides/isolates — prevents RLO spoofing in notification banners.
            if (v >= 0x202A && v <= 0x202E) || (v >= 0x2066 && v <= 0x2069) { continue }
            // Strip zero-width chars — prevents invisible payload smuggling.
            if (v >= 0x200B && v <= 0x200D) || v == 0x2060 || v == 0xFEFF { continue }
            out.append(scalar)
            count += 1
        }
        return String(out)
    }

    /// Parse an OSC 777 payload (minus the "777;" prefix) into (title, body).
    /// Format: `notify;<title>;<body>`. Returns nil for malformed payloads.
    static func parseOSC777(_ text: String) -> (title: String, body: String)? {
        let parts = text.split(separator: ";", maxSplits: 2,
                               omittingEmptySubsequences: false).map(String.init)
        guard parts.first == "notify", parts.count >= 2 else { return nil }
        return (title: parts[1], body: parts.count >= 3 ? parts[2] : "")
    }

    /// Parse an OSC 133 FinalTerm shell-integration payload.
    /// A = prompt start, B = command start, C = command executed, D;<exit> = finished.
    enum OSC133Event: Equatable {
        case promptStart
        case commandStart
        case commandFinished(exitCode: Int?)
    }

    static func parseOSC133(_ text: String) -> OSC133Event? {
        guard let first = text.first else { return nil }
        switch first {
        case "A": return .promptStart
        case "B": return .commandStart
        case "D":
            let parts = text.split(separator: ";", maxSplits: 1)
            let exit: Int?
            if parts.count > 1, let raw = Int(parts[1]) {
                // Clamp to valid POSIX exit-code range (0–255) so an
                // out-of-range PTY-supplied value cannot propagate to UI
                // arithmetic downstream.
                if (0...255).contains(raw) {
                    exit = raw
                } else {
                    // An integer outside 0–255 is not a valid POSIX exit code.
                    // Log it: differentiates a crafted out-of-range payload from a
                    // legitimately absent exit code (both otherwise yield exit=nil).
                    securityLog.warning("OSC 133 D: PTY exit code \(raw, privacy: .public) out of POSIX range 0–255; treating as unknown")
                    exit = nil
                }
            } else if parts.count > 1 {
                // A non-integer D;<value> payload is malformed; log it so
                // spoofing attempts are distinguishable from a missing exit code.
                securityLog.warning("OSC 133 D: malformed exit code '\(String(parts[1]).prefix(32), privacy: .public)'; treating as unknown")
                exit = nil
            } else {
                exit = nil
            }
            return .commandFinished(exitCode: exit)
        default: return nil
        }
    }

    override func bell(source: Terminal) {
        super.bell(source: source)
        Task { @MainActor [weak self] in
            self?.onBell?()
        }
    }

    /// Intercept paste to warn before sending multi-line clipboard content to the PTY.
    /// Each newline acts as Enter in the terminal and may immediately execute whatever
    /// text precedes it — clipboard-poisoned content could silently run commands.
    /// Skipped when bracketed-paste mode is active; the shell handles newlines safely
    /// in that mode by not executing until the user presses Enter explicitly.
    override func paste(_ sender: Any?) {
        // SwiftTerm declares paste(_:) as `Any` (non-optional) so coerce
        // explicitly to silence the Any?→Any warning.
        let s: Any = sender ?? NSNull()
        guard !getTerminal().bracketedPasteMode,
              let text = NSPasteboard.general.string(forType: .string),
              text.contains("\n") || text.contains("\r") else {
            super.paste(s)
            return
        }
        let newlineCount = text.unicodeScalars.filter { $0 == "\n" || $0 == "\r" }.count
        let alert = NSAlert()
        alert.messageText = "Paste \(newlineCount) Line\(newlineCount == 1 ? "" : "s")?"
        alert.informativeText = "The clipboard contains newlines. Each newline acts as Enter in the terminal and may execute commands immediately."
        alert.alertStyle = .warning
        alert.addButton(withTitle: "Paste")
        alert.addButton(withTitle: "Cancel")
        guard alert.runModal() == .alertFirstButtonReturn else { return }
        super.paste(s)
    }

    /// Open a link resolved from an OSC 8 payload or from implicit link detection.
    /// SwiftTerm's default only handles URLs with a scheme — plain file paths like
    /// `Mosaic/App/AppDelegate.swift` or `src/foo.rs:42:10` fall through and error
    /// out. We resolve them against the terminal's cwd and open the file.
    public func requestOpenLink(source _: TerminalView, link: String, params _: [String: String]) {
        let cwd = enclosingTerminalWindowView?.currentCwd
            ?? FileManager.default.homeDirectoryForCurrentUser.path
        guard let url = Self.resolveOpenableURL(for: link, cwd: cwd) else { return }

        // Rate-limit OSC 8 link-open confirmation dialogs to ≤ 5 per minute.
        // A hostile PTY can emit unbounded OSC 8 sequences; without a cap, each
        // one blocks the main thread with a modal NSAlert (DoS + alert fatigue).
        let now = ProcessInfo.processInfo.systemUptime
        if now - linkOpenWindowStart >= 60 {
            linkOpenCount = 0
            linkOpenWindowStart = now
        }
        guard linkOpenCount < 5 else {
            securityLog.warning("link-open rate-limit reached; dropping PTY-requested open of \(url.absoluteString, privacy: .private)")
            return
        }
        linkOpenCount += 1

        // Always confirm before opening PTY-supplied links. Terminal output is untrusted;
        // a crafted OSC 8 payload could target local files or app-registered URL schemes.
        let alert = NSAlert()
        alert.messageText = "Open Link?"
        // Show the full URL so the user sees exactly what will be opened.
        // Truncating the display string would let a crafted URL hide a malicious
        // suffix behind the ellipsis while NSWorkspace.open() receives the full URL.
        alert.informativeText = "Terminal wants to open:\n\(url.absoluteString)"
        alert.alertStyle = .informational
        alert.addButton(withTitle: "Open")
        alert.addButton(withTitle: "Cancel")
        guard alert.runModal() == .alertFirstButtonReturn else {
            securityLog.info("link-open cancelled by user: \(url.absoluteString, privacy: .private)")
            return
        }

        // Re-check existence just before opening to narrow the TOCTOU window
        // between resolveOpenableURL (called before the dialog was shown) and
        // the NSWorkspace.open() call. During a long confirmation dialog a
        // concurrent process could atomically replace a file or symlink target.
        if url.isFileURL {
            guard FileManager.default.fileExists(atPath: url.path) else {
                securityLog.warning("link-open aborted: file no longer exists at \(url.path, privacy: .private)")
                return
            }
        }

        securityLog.info("link-open confirmed by user: \(url.absoluteString, privacy: .private)")
        NSWorkspace.shared.open(url)
    }

    /// Schemes that are categorically blocked even with the user-confirmation
    /// dialog. The dialog is the primary mitigation; this deny-list provides
    /// defence-in-depth for schemes that must never reach NSWorkspace.
    ///
    /// **Do not flip to an allow-list.** See `Docs/ADR/006-link-handling.md`
    /// for the rationale — custom app schemes (vscode://, slack://, cursor://,
    /// zoom://, obsidian://, …) are legitimate user clicks; the confirmation
    /// dialog in `requestOpenLink` is the social-engineering defence.
    /// `file://` is handled separately via path-resolution below.
    static let deniedLinkSchemes: Set<String> = [
        "javascript", "data", "vbscript",   // script injection
        "jar", "ms-its",                     // historic code-execution vectors
    ]

    /// Pure function so we can unit-test without an NSWorkspace side effect.
    static func resolveOpenableURL(for link: String, cwd: String) -> URL? {
        // Reject pathologically long inputs before any regex / canonicalisation
        // work — both inputs are PTY-controlled and unbounded.
        guard link.utf8.count <= 4096, cwd.utf8.count <= 4096 else { return nil }
        let trimmed = link.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmed.isEmpty else { return nil }

        // Classify as URL vs. filesystem path. URL(string:) parses "hello.txt:42"
        // as scheme="hello.txt" because the scheme grammar allows dots; gate on
        // an explicit "://" so compiler-output paths aren't mis-classified.
        // Opaque schemes like `mailto:` are accepted in the second branch.
        var pathBody = trimmed
        if trimmed.contains("://"), let url = URL(string: trimmed),
           let scheme = url.scheme?.lowercased() {
            if scheme == "file" {
                // Unwrap to the underlying path and fall through so the same
                // canonicalise + exists-check applies.
                pathBody = url.path
            } else if deniedLinkSchemes.contains(scheme) {
                securityLog.warning("link-open blocked: PTY requested denied scheme '\(scheme, privacy: .public)'")
                return nil
            } else {
                return url
            }
        } else if let colon = trimmed.firstIndex(of: ":"),
                  let url = URL(string: trimmed),
                  let scheme = url.scheme?.lowercased(),
                  // Avoid mis-classifying "hello.txt:42" — scheme must look like
                  // a real scheme (letters only, no dots) for the opaque branch.
                  trimmed[..<colon].allSatisfy({ $0.isASCII && $0.isLetter }),
                  !deniedLinkSchemes.contains(scheme) {
            return url
        }

        // Strip trailing `:line` or `:line:col` suffix (compiler output).
        let path = pathBody.replacingOccurrences(
            of: #":[0-9]+(?::[0-9]+)?$"#,
            with: "",
            options: .regularExpression)

        // Expand ~ and resolve against cwd for relative paths.
        let expanded = (path as NSString).expandingTildeInPath
        let joined = expanded.hasPrefix("/")
            ? expanded
            : (cwd as NSString).appendingPathComponent(expanded)

        // Canonicalise to fold out `..` segments and symlinks before exists-check.
        // No path-containment check here by design — see
        // `Docs/ADR/006-link-handling.md`. The confirmation dialog in
        // `requestOpenLink` shows the resolved path before NSWorkspace.open().
        let resolved = URL(fileURLWithPath: joined)
            .resolvingSymlinksInPath().standardizedFileURL.path
        var isDir: ObjCBool = false
        guard FileManager.default.fileExists(atPath: resolved, isDirectory: &isDir) else { return nil }
        return URL(fileURLWithPath: resolved, isDirectory: isDir.boolValue)
    }

    /// Route find-panel actions to our custom find bar. SwiftTerm's bundled find UI
    /// (added with the GPU backend) hangs the main thread when shown inside our world-
    /// space terminal layout, so we don't call super.
    override func performFindPanelAction(_ sender: Any?) {
        guard let item = sender as? NSMenuItem,
              let host = enclosingTerminalWindowView else { return }
        switch item.tag {
        case Int(NSFindPanelAction.showFindPanel.rawValue):
            host.showFindBar()
        case Int(NSFindPanelAction.next.rawValue):
            host.findNext()
        case Int(NSFindPanelAction.previous.rawValue):
            host.findPrevious()
        default:
            break
        }
    }

    fileprivate var enclosingTerminalWindowView: TerminalWindowView? {
        var view: NSView? = superview
        while let v = view {
            if let tw = v as? TerminalWindowView { return tw }
            view = v.superview
        }
        return nil
    }
}

