    func applyTheme(_ theme: Theme) {
        self.theme = theme
        termView.nativeForegroundColor = theme.terminalForeground
        termView.nativeBackgroundColor = theme.terminalBackground
        termView.font = theme.terminalFont
        termView.feed(text: theme.oscSequences)
        layer?.backgroundColor = theme.terminalBackground.cgColor
        // syncTitleBarColor preserves the broadcast tint when active; calling
        // titleBar.applyTheme directly would clobber it.
        syncTitleBarColor()
        updateBorder()
    }

    private func syncTitleBarColor() {
        if isBroadcast {
            titleBar.layer?.backgroundColor = NSColor(red: 0.15, green: 0.3, blue: 0.15, alpha: 1).cgColor
        } else {
            titleBar.applyTheme(background: theme.terminalBackground, foreground: theme.terminalForeground)
        }
    }

    // MARK: - Resize handles

    private func setupResizeHandles() {
        for edge in ResizeHandleView.Edge.allCases {
            let h = ResizeHandleView(edge: edge)
            h.translatesAutoresizingMaskIntoConstraints = false
            addSubview(h)
            h.installConstraints(in: self)
            h.onResizeBegan = { [weak self] in
                guard let self else { return }
                frameBeforeDrag = frame
            }
            h.onResize = { [weak self] dx, dy in
                self?.handleResize(edge: edge, screenDX: dx, screenDY: dy)
            }
            h.onResizeEnded = { [weak self] in
                guard let self else { return }
                clearSnapGuides?()
                guard frame != frameBeforeDrag else { return }
                onMoveEnded?(frameBeforeDrag, frame)
            }
        }
    }

    private func handleResize(edge: ResizeHandleView.Edge, screenDX: CGFloat, screenDY: CGFloat) {
        guard let cv = canvasView else { return }
        let zoom = cv.currentZoom
        let dx = screenDX / zoom
        // screenDY is window-space y-up; world space is y-down, so negate
        let dy = -screenDY / zoom

        var r = frame
        switch edge {
        case .topLeft:
            r.origin.x += dx; r.size.width -= dx
            r.origin.y += dy; r.size.height -= dy
        case .top:
            r.origin.y += dy; r.size.height -= dy
        case .topRight:
            r.size.width += dx
            r.origin.y += dy; r.size.height -= dy
        case .left:
            r.origin.x += dx; r.size.width -= dx
        case .right:
            r.size.width += dx
        case .bottomLeft:
            r.origin.x += dx; r.size.width -= dx
            r.size.height += dy
        case .bottom:
            r.size.height += dy
        case .bottomRight:
            r.size.width += dx
            r.size.height += dy
        }

        r.size.width  = max(r.size.width,  minSize.width)
        r.size.height = max(r.size.height, minSize.height)

        // Apply edge-aware snapping: call snapFrame to find alignment deltas, then
        // apply each delta to the dimension that's actually moving for this edge.
        if let snap = snapFrame {
            let snapped = snap(r, edge)
            let sdx = snapped.origin.x - r.origin.x
            let sdy = snapped.origin.y - r.origin.y

            let dragsRight  = edge == .right  || edge == .topRight    || edge == .bottomRight
            let dragsLeft   = edge == .left   || edge == .topLeft     || edge == .bottomLeft
            let dragsBottom = edge == .bottom || edge == .bottomLeft  || edge == .bottomRight
            let dragsTop    = edge == .top    || edge == .topLeft     || edge == .topRight

            if dragsRight       { r.size.width  += sdx }
            else if dragsLeft   { r.origin.x    += sdx; r.size.width  -= sdx }
            if dragsBottom      { r.size.height += sdy }
            else if dragsTop    { r.origin.y    += sdy; r.size.height -= sdy }

            r.size.width  = max(r.size.width,  minSize.width)
            r.size.height = max(r.size.height, minSize.height)
        }

        frame = r
        onMoved?()
    }

    // MARK: - Focus

    func focusTerminal() {
        window?.makeFirstResponder(termView)
    }

    // SwiftTerm's pendingDisplay throttle races between background feeds and
    // main-thread resets; long-running apps occasionally see it stick `true`,
    // freezing the view until a scroll forces setNeedsDisplay. Nudge AppKit
    // ourselves whenever the buffer has unrendered updates.
    private var redrawWatchdog: DispatchSourceTimer?

    private func startRedrawWatchdog() {
        let t = DispatchSource.makeTimerSource(queue: .main)
        t.schedule(deadline: .now() + 1, repeating: 1, leeway: .milliseconds(200))
        t.setEventHandler { [weak self] in
            guard let self, self.termView.getTerminal().getUpdateRange() != nil else { return }
            self.termView.needsDisplay = true
        }
        t.resume()
        redrawWatchdog = t
    }

    deinit { redrawWatchdog?.cancel() }

    // MARK: - Input broadcasting

    func sendInput(_ data: Data) {
        let bytes = [UInt8](data)
        termView.suppressBroadcast = true
        termView.send(data: bytes[...])
        termView.suppressBroadcast = false
    }

    // MARK: - Visual state

    func setBroadcastHighlight(_ on: Bool) {
        isBroadcast = on
        syncTitleBarColor()
    }

    func terminate() {
        // SwiftTerm tears down the PTY when the view is deallocated,
        // but we can signal the process to exit cleanly.
        termView.send(data: [0x04][...])  // Ctrl-D / EOF
    }
}

// MARK: - LocalProcessTerminalViewDelegate

extension TerminalWindowView: LocalProcessTerminalViewDelegate {
    nonisolated func sizeChanged(source: LocalProcessTerminalView, newCols: Int, newRows: Int) {
        // SIGWINCH is sent automatically by SwiftTerm; nothing extra needed here
    }

    nonisolated func setTerminalTitle(source: LocalProcessTerminalView, title: String) {
        // Cap length and sanitize PTY-supplied title: strip bidi overrides (prevents
        // RLO spoofing) and control bytes (prevents title-bar injection / unbounded
        // allocation). Capping before sanitizing bounds the work done here.
        let raw = title.count > 256 ? String(title.prefix(256)) : title
        let safe = InterceptingTerminalView.sanitizeNotificationText(
            InterceptingTerminalView.sanitizeForClipboard(raw), max: 256)
        Task { @MainActor [weak self] in
            self?.shellReady = true
            self?.currentTitle = safe
            self?.titleBar.titleLabel.stringValue = safe
            self?.syncTitleBarColor()
        }
    }

    nonisolated func hostCurrentDirectoryUpdate(source: TerminalView, directory: String?) {
        guard let dir = directory else { return }
        // Reject oversized OSC 7 payloads before any URL parsing or filesystem work.
        // A crafted multi-megabyte payload would cause unbounded heap allocation
        // inside URL(string:) and potentially expensive subsequent fs calls.
        // 4096 bytes is generous for any real working directory path.
        guard dir.utf8.count <= 4096 else {
            securityLog.warning("OSC 7 cwd payload exceeds 4096 bytes (\(dir.utf8.count, privacy: .public)); ignoring")
            return
        }
        // SwiftTerm passes a file:// URL; extract the plain POSIX path.
        let path = URL(string: dir)?.path ?? dir
        // Validate before storing. An adversarial PTY could emit crafted OSC 7 payloads
        // to redirect resolveOpenableURL() (used for relative OSC 8 targets) to any
        // existing path on disk — e.g. pointing it at /tmp/payload then sending an
        // OSC 8 link of "file.txt" would open /tmp/payload/file.txt.
        guard !path.isEmpty, !path.contains("\0") else { return }
        // Restrict to home subtree so a crafted OSC 7 cannot give currentCwd full
        // filesystem reach and redirect subsequent OSC 8 link opens outside ~/…
        // Resolve symlinks in both paths before comparing so a symlink inside home
        // that points outside (possible on case-insensitive APFS) cannot bypass
        // the containment check via hasPrefix on the un-resolved strings.
        let resolvedPath = URL(fileURLWithPath: path).resolvingSymlinksInPath().path
        let home = URL(fileURLWithPath: FileManager.default.homeDirectoryForCurrentUser.path)
            .resolvingSymlinksInPath().path
        guard resolvedPath == home || resolvedPath.hasPrefix(home + "/") else { return }
        var isDir: ObjCBool = false
        guard FileManager.default.fileExists(atPath: resolvedPath, isDirectory: &isDir),
              isDir.boolValue else { return }
        Task { @MainActor [weak self] in
            self?.currentCwd = resolvedPath
        }
    }

    nonisolated func processTerminated(source: TerminalView, exitCode: Int32?) {
        Task { @MainActor [weak self] in
            self?.onClose?()
        }
    }
}


